  public drawLine(context, line: string) {
    const points = this.linePoints[line];

    if (points !== undefined) {
      context.beginPath();
      context.strokeStyle = points [points.length - 1];
      context.lineWidth = 3;
      context.moveTo(points[0][0], points[0][1]);
      for (let i = 1; i < points.length - 1; i++) {
        context.lineTo(points[i][0], points[i][1]);
      }
      context.stroke();
      context.save();
      context.font = '20pt Calibri';
      context.fillStyle = points [points.length - 1];
      context.fillText(1 + Number(line), points[0][0] - 20, points[0][1] + 10);
    }
  }


